export const DateTh = (strdate = null, timezone = 7) => {
  let date = (strdate !== null) ? new Date(strdate.toString()) : new Date();

  let utc = date.getTime() + (date.getTimezoneOffset() * 60000);
  date = new Date(utc + (3600000 * (timezone)));

  let year = date.getFullYear();
  let month = date.getMonth() + 1;
  let new_mount = month;
  if (month <= 9) {
    new_mount = '0' + month;
  }

  let day = date.getDate();
  let hours = date.getHours();
  let minutes = date.getMinutes();
  let seconds = date.getSeconds();

  let new_day = day;
  if (day <= 9) {
    new_day = '0' + new_day;
  }

  let new_hours = hours;
  if (hours <= 9) {
    new_hours = '0' + new_hours;
  }

  let new_minutes = minutes;
  if (new_minutes <= 9) {
    new_minutes = '0' + new_minutes;
  }

  let new_sec = seconds;
  if (new_sec <= 9) {
    new_sec = '0' + new_sec;
  }

  return `${year}-${new_mount}-${new_day} ${new_hours}:${new_minutes}:${new_sec}`;

}
